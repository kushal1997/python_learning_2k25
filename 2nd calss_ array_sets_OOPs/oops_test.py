# Object-Oriented Programming (OOPs) is a programming paradigm used in Python where programs are organized around objects that interact
# with each other. It emphasizes code reusability, encapsulation, and modularity. Objects are instances of classes 
# that define their properties (attributes) and behavior (methods). OOPs allows 
# for creation of complex systems by modeling real-world entities, promoting code organization, 
# and facilitating maintenance and extensibility.

